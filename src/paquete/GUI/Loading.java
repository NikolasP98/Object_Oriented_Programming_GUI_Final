    public void LoadingScreen(String a, int b){

        this.setVisible(true);
        this.jLabel1.setText(a);
        
        try{
            for(int i=0; i<=100; i++){
                
                Random rand = new Random();
                int alea = rand.nextInt(b);
                
                Thread.sleep(alea);

                this.Loading_bar.setValue(i);
                
                this.Percent_txt.setText(String.valueOf(i) + "%");
                
            }
            
            try {
                Thread.sleep(500);
            } catch (Exception e) {
                System.out.println("error en dormir");
            }
            
            this.dispose();
            
        } catch (Exception e) {
            System.out.println("error en cargar");
        }
        
    }
    
